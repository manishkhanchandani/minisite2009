            <p class="bodytext">Auctions 4 a Cause, LLC, a pioneer in the US for 
              &quot;Highest Unique Bid&quot; Auctions, has been holding Unique 
              Bid Auctions since the Fall of 2003. We were the first unique bid 
              auction site to donate any auction proceeds to charity, not only 
              that, but we continue to lead the pack by donating at least 25% 
              of our auction proceeds to the charities we support. By becoming 
              a member, you get to influence how much your selected charity receives 
              by placing bids in our auctions. At Auctions 4 a Cause you can bid 
              with confidence along with over 100,000 members and a proven and 
              reputable company you know has been around for years.</p>
            <p class="bodytext">Over the years, Auctions 4 a Cause has donated 
              tens of thousands of dollars to multiple charities (see the <a href="charities.asp">Charities 
