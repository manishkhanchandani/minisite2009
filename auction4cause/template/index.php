              to people like you who participate in our auctions. By bidding, 
              not only do you get the opportunity to get amazing deals, but you 
              also know you are supporting worthy and needy charities.</p>

            <p class="bodytext">Auctions4aCause.com features unique bid auctions 
              which are legally classified as skill-based prize competitions. 
              In these blind auctions our members compete for the chance to purchase 
              popular brand new, name brand products at 80-90% off retail prices. 
              The participants bid for a particular item by submitting <i>blind 
              </i>or<i> sealed</i> (i.e. secret) bids: the winner is the bidder 
              who places the highest unique bid. A &quot;unique&quot; bid, in 
              this context, means one in which the amount offered is the only 
              one of that value. Each auction has a defined number of bids that 
              are accepted before it closes and the highest unique bidder gets 
              to purchase the item for their bid amount. For every bid a participant 
              places in an auction, they pay a nominal bidding fee and the charity 
              in the participant's member profile will receive a donation based 
              on their bid(s).</p>
            <p class="bodytext">Prior to starting Auctions 4 a Cause, several 
              of our founders were involved with fundraising for charities and 
              had noticed that contributions to charities were suffering. The 
              company was founded after the high-tech industry crash in 2003 in 
              which we saw first hand a donations shortfall and increased need 
              for charitable support. We decided to take matters into our own 
              hands and use years of marketing, engineering and web development 
              experience in pursuit of bettering our society and achieving our 
              dreams. </p>
